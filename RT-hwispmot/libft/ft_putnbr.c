#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;
	int		sign;

	sign = 1;
	if (n < 0)
	{
		write(1, "-", 1);
		sign = -1;
	}
	if (n / 10 == 0)
	{
		c = n * sign + 48;
		write(1, &c, 1);
	}
	else
	{
		ft_putnbr(sign * (n / 10));
		ft_putnbr(sign * (n % 10));
	}
}
